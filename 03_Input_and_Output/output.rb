# There are two different commands for outputing to the terminal
# Print
print "Hello, "

# Puts
puts "World!"

# You may wonder how 2 prints end up on the same line.
# Thats because the print command prints everything on one line. The puts (put string) command prints its parameter and a newline. 
# Heres a demonstration using a semicolon. Its never required but it allows multiple commands on one line
puts "Command 1"; puts "Command 2"

# As you see the second command is printed on a newline thanks to the first puts command

# Prints also have returns. Both puts and prints return nil
  