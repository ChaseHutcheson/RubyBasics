# Arithmetic
# Addition
puts "1 + 2 = #{1 + 2}"

# Subtraction
puts "1 - 2 = #{1 - 2}"

# Multiplication
puts "1 * 2 = #{1 * 2}"

# Division
puts "1 / 2 = #{1 / 2}"

# Exponents
puts "2 ** 3 = #{2 ** 3}"

# Modulus / Modulo
puts "8 % 3 = #{8 % 3}", "\n"

# Integers and Floats (NOTE: Arithmetic always results in Integers)
# Integer
puts "Integer (Whole Number): #{17}"

# Float
puts "Float (Decimal): #{17.9}", "\n"

# Converting number types
# Converting an Integer to Float:
puts 13.to_f

# Converting a Float to Integer (Floats round down):
puts 13.to_i
puts 13.9.to_i, "\n"

# Useful Number Methods
puts 13.even?
puts 14.even?

puts 13.odd?
puts 14.odd?